import json, sys, io, os
import sys

# Fix stdout encoding for Windows
sys.stdout = io.TextIOWrapper(sys.stdout.buffer, encoding='utf-8')

# Load DB
with open('fitness-andrey/exercisedb_data/exercise_db_final.json', 'r', encoding='utf-8') as f:
    db = json.load(f)
db_dict = {e['exerciseId']: e for e in db}


def ex(eid, sets, reps, rest_sec, rpe, tips, warning=None, alternatives=None):
    """Build exercise entry from DB."""
    e = db_dict[eid]
    return {
        "exerciseId": eid,
        "nameRu": e['nameRu'],
        "sets": sets,
        "reps": reps,
        "rest_sec": rest_sec,
        "rpe": rpe,
        "tips": tips,
        "warning": warning,
        "alternatives": alternatives or [],
        "gifUrl": e['gifUrl']
    }


def alt(eid):
    e = db_dict[eid]
    return {"exerciseId": eid, "nameRu": e['nameRu']}


