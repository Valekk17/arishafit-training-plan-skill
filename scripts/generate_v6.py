def ex(eid, name, sets, reps, rest, rpe, tips, warning, alts):
    """Создать exercise dict. alts — список (eid, name, tips, warning)."""
    return {
        "exerciseId": eid,
        "nameRu": name,
        "sets": sets,
        "reps": reps,
        "rest_sec": rest,
        "rpe": rpe,
        "tips": tips,
        "warning": warning,
        "gifUrl": gif(eid),
        "alternatives": [
            {"exerciseId": a[0], "nameRu": a[1], "tips": a[2], "warning": a[3], "gifUrl": gif(a[0])}
            for a in alts
        ],
    }


